// PRIMITIVE TYPES

// String
const name = 'Umesh';
console.log(typeof name);

// Numbers
const age = 100;
console.log(typeof age);

// Boolean
const hasKids = true
console.log(typeof hasKids);

// Null
const car = null;
console.log(typeof car);

// undefined (note use of let instead of const)
let test;
console.log(typeof test);

// Symbol
const sym = Symbol();
console.log(typeof sym);

// REFERENCE TYPES
// Array
const hobbies = ['movies', 'music'];
console.log(typeof hobbies);

// Object literal
const address = {
    city: 'New York',
    state: 'NY'
}
console.log(typeof address);

// Date
const today = new Date();
console.log(today);
console.log(typeof today);